<h2>Confession App: Catholic</h2>
<p>
The Confession App prepares you in a gentle and comforting way for the Catholic sacrament of Confession (Reconciliation). Place a check mark next to items you'd like to confess and they appear in the Confession Guide section for use during Confession. 
Written with new penititant in mind, the tone and wording in the app is full of 
grace and forgiveness. However, the app is not lightweight. It covers all of the 
major and tough questions to help keep even the most experienced user engaged. 
App data is never stored permanently to the phone.</p>
